public class Calculator {
    int num1 = 5;
    int num2 = 10;

    

    public int cong() {
        int a = num1 + num2;
        return a;
    }

    public int tru() {
        int a = num1 - num2;
        return a;
    }

    public int nhan() {
        int a = num1 * num2;
        return a;
    }

    public float chia() {
        float a = (float) num1 / num2;
        return a;
    }

}
